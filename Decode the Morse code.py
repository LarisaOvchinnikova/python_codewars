def decodeMorse(morse_code):
    #print(MORSE_CODE)
    lst_of_words = morse_code.split('   ')
    res = ''
    for word in lst_of_words:
        word = word.split(' ')
        print(word)
        for char in word:
            res = res + MORSE_CODE[char]
        res = res + " "
    return res[0:-1]

